
public class ArithmeticOperators {
	
		//Using Arithmetic Operators
	
		static void addition() {
			System.out.println(5+5); //Using Addition Operator
		}
		
		static void subtraction() {
			System.out.println(5-4);  //Using Subtraction Operator
		}
		
		static void multiplication() {
			System.out.println(5*5);   //Using Multiplication Operator
		}
		
		static void division() {
			System.out.println(120/5);  //Using Division Operator
		}
		
		static void modulus() {
			System.out.println(20%3);   //Using Modulus Operator
		}


	public static void main(String[] args) {
		
		addition();	 //Calling Addition method
		subtraction();  //Calling Subtraction method
		multiplication();  //Calling Multiplication method
		division();		//Calling Division method
		modulus();		// Calling Modulus method


	}

}
